    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String keyword = txtSearch.getText();
javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) accountTable.getModel();
model.setRowCount(0);

String sql = "SELECT a.account_id, c.first_name, c.last_name, a.account_type, a.balance " +
             "FROM Account a JOIN Customer c ON a.customer_id = c.customer_id " +
             "WHERE c.first_name LIKE ? OR c.last_name LIKE ? OR a.account_id = ?";

try (java.sql.Connection conn = DBConnection.getConnection();
     java.sql.PreparedStatement pstmt = conn.prepareStatement(sql)) {
    
    pstmt.setString(1, "%" + keyword + "%");
    pstmt.setString(2, "%" + keyword + "%");
    
    // If search is not a number, pass 0 for account_id check to prevent crashes
    int lookupId = 0;
    try { lookupId = Integer.parseInt(keyword); } catch(NumberFormatException e){}
    pstmt.setInt(3, lookupId);
    
    java.sql.ResultSet rs = pstmt.executeQuery();
    while(rs.next()) {
        model.addRow(new Object[]{
            rs.getInt("account_id"),
            rs.getString("first_name"),
            rs.getString("last_name"),
            rs.getString("account_type"),
            rs.getBigDecimal("balance")
        });
    }
} catch (Exception ex) {
    System.out.println("Search error: " + ex.getMessage());
}
    }//GEN-LAST:event_txtSearchKeyReleased

