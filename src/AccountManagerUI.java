    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String fName = txtFirstName.getText();
String lName = txtLastName.getText();
String email = txtEmail.getText();
String phone = txtPhone.getText();
String acctType = (String) cmbAccountType.getSelectedItem();
String balanceStr = txtBalance.getText();

if(fName.isEmpty() || lName.isEmpty() || balanceStr.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all mandatory fields.");
    return;
}

try (java.sql.Connection conn = DBConnection.getConnection()) {
    // Turn off auto-commit so both saves succeed together like a real bank transaction
    conn.setAutoCommit(false);
    
    // 1. Insert into Customer Table
    String customerSql = "INSERT INTO Customer (first_name, last_name, email, phone_number) VALUES (?, ?, ?, ?)";
    java.sql.PreparedStatement cStmt = conn.prepareStatement(customerSql, java.sql.Statement.RETURN_GENERATED_KEYS);
    cStmt.setString(1, fName);
    cStmt.setString(2, lName);
    cStmt.setString(3, email);
    cStmt.setString(4, phone);
    cStmt.executeUpdate();
    
    // Retrieve the customer_id generated by MySQL
    java.sql.ResultSet rs = cStmt.getGeneratedKeys();
    int customerId = 0;
    if (rs.next()) {
        customerId = rs.getInt(1);
    }
    
    // 2. Insert into Account Table linked to that Customer
    String accountSql = "INSERT INTO Account (customer_id, account_type, balance) VALUES (?, ?, ?)";
    java.sql.PreparedStatement aStmt = conn.prepareStatement(accountSql);
    aStmt.setInt(1, customerId);
    aStmt.setString(2, acctType);
    aStmt.setDouble(3, Double.parseDouble(balanceStr));
    aStmt.executeUpdate();
    
    conn.commit(); // Save changes permanently
    javax.swing.JOptionPane.showMessageDialog(this, "Account opened successfully!");
    refreshTable();
} catch (Exception ex) {
    javax.swing.JOptionPane.showMessageDialog(this, "Error processing transaction: " + ex.getMessage());
}
        
    }//GEN-LAST:event_btnAddActionPerformed

