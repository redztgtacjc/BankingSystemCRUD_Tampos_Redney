    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = accountTable.getSelectedRow();
if (selectedRow == -1) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please select an account row from the table first.");
    return;
}

String acctId = accountTable.getValueAt(selectedRow, 0).toString();
String fName = txtFirstName.getText();
String lName = txtLastName.getText();
String acctType = (String) cmbAccountType.getSelectedItem();

// Update customer names and account metadata settings
String updateCustomerSql = "UPDATE Customer c JOIN Account a ON c.customer_id = a.customer_id SET c.first_name = ?, c.last_name = ? WHERE a.account_id = ?";
String updateAccountSql = "UPDATE Account SET account_type = ? WHERE account_id = ?";

try (java.sql.Connection conn = DBConnection.getConnection()) {
    java.sql.PreparedStatement p1 = conn.prepareStatement(updateCustomerSql);
    p1.setString(1, fName);
    p1.setString(2, lName);
    p1.setInt(3, Integer.parseInt(acctId));
    p1.executeUpdate();
    
    java.sql.PreparedStatement p2 = conn.prepareStatement(updateAccountSql);
    p2.setString(1, acctType);
    p2.setInt(2, Integer.parseInt(acctId));
    p2.executeUpdate();
    
    javax.swing.JOptionPane.showMessageDialog(this, "Account updated successfully.");
    refreshTable();
} catch (Exception ex) {
    javax.swing.JOptionPane.showMessageDialog(this, "Update error: " + ex.getMessage());
}
    }//GEN-LAST:event_btnUpdateActionPerformed

