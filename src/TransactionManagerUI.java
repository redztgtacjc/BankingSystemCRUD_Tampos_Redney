// 1. Loads all existing Account IDs into your dropdown selection menu
private void populateAccountDropdown() {
    cmbAccount.removeAllItems();
    String sql = "SELECT account_id FROM Account";
    try (java.sql.Connection conn = DBConnection.getConnection();
         java.sql.Statement stmt = conn.createStatement();
         java.sql.ResultSet rs = stmt.executeQuery(sql)) {
        
        while (rs.next()) {
            cmbAccount.addItem(rs.getString("account_id"));
        }
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading accounts: " + ex.getMessage());
    }
}

// 2. Loads transaction logs into your table grid view
private void refreshTable() {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) transactionTable.getModel();
    model.setRowCount(0); // Wipe old lines
    
    String sql = "SELECT * FROM Transaction";
    try (java.sql.Connection conn = DBConnection.getConnection();
         java.sql.Statement stmt = conn.createStatement();
         java.sql.ResultSet rs = stmt.executeQuery(sql)) {
        
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("transaction_id"),
                rs.getInt("account_id"),
                rs.getString("transaction_type"),
                rs.getBigDecimal("amount"),
                rs.getDate("transaction_date")
            });
        }
    } catch (Exception ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading history: " + ex.getMessage());
    }
}
