    private void btnWithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWithdrawActionPerformed
        // TODO add your handling code here:
        String accountId = (String) cmbAccount.getSelectedItem();
String amountStr = txtAmount.getText();

if (accountId == null || amountStr.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Please select an account and enter an amount.");
    return;
}

double amount = Double.parseDouble(amountStr);

try (java.sql.Connection conn = DBConnection.getConnection()) {
    conn.setAutoCommit(false);
    
    // 1. CHECK CURRENT BALANCE FIRST: See if account has enough funds available
    String checkBalanceSql = "SELECT balance FROM Account WHERE account_id = ?";
    java.sql.PreparedStatement checkStmt = conn.prepareStatement(checkBalanceSql);
    checkStmt.setInt(1, Integer.parseInt(accountId));
    java.sql.ResultSet rs = checkStmt.executeQuery();
    
    if (rs.next()) {
        double currentBalance = rs.getDouble("balance");
        if (currentBalance < amount) {
            // STOP! Block withdrawal due to insufficient funds error
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Insufficient funds! Current balance: $" + currentBalance);
            return;
        }
    }
    
    // 2. DEDUCT MONEY: If they have enough, proceed with balance deduction
    String deductSql = "UPDATE Account SET balance = balance - ? WHERE account_id = ?";
    java.sql.PreparedStatement p1 = conn.prepareStatement(deductSql);
    p1.setDouble(1, amount);
    p1.setInt(2, Integer.parseInt(accountId));
    p1.executeUpdate();
    
    // 3. LOG TRANSACTION HISTORY entry
    String logSql = "INSERT INTO Transaction (account_id, transaction_type, amount, transaction_date) VALUES (?, 'Withdraw', ?, CURDATE())";
    java.sql.PreparedStatement p2 = conn.prepareStatement(logSql);
    p2.setInt(1, Integer.parseInt(accountId));
    p2.setDouble(2, amount);
    p2.executeUpdate();
    
    conn.commit();
    javax.swing.JOptionPane.showMessageDialog(this, "Successfully withdrew $" + amount);
    txtAmount.setText("");
    refreshTable();
} catch (Exception ex) {
    javax.swing.JOptionPane.showMessageDialog(this, "Withdrawal error: " + ex.getMessage());
}
    }//GEN-LAST:event_btnWithdrawActionPerformed

